package org.example;

public class CalculatorDemo {
    public int calculateSumTwoValues (int num1,int num2){
        return num1 + num2;
        }
    public int calculateSubtractionTwoValues (int num1, int num2){
        return num1 - num2;
    }
    public int calculateMultiplicationTwoValues (int num1, int num2){
        return num1 * num2;
    }
    public int calculateDivisionTwoValues (int num1, int num2){
        return num1 / num2;
    }
}
